const {resolve} = require("path");
const {writeFileSync} = require("fs");
const {properties, units} = require("mdn-data/css");
const camelCase = require("camelcase");

const quote = str => `"${str}"`;

const content = `// generated by script

export const properties = [${Object.keys(properties)
    .map(camelCase)
    .filter(prop => !["*"].includes(prop))
    .map(quote)
    .join(", ")}];

export const rawProperties=[${Object.keys(properties)
    .filter(prop => !["--*"].includes(prop))
    .map(quote)
    .join(", ")}];

export const units = [${Object.keys(units)
    .map(quote)
    .join(", ")}];
`;

writeFileSync(resolve(__dirname, "../src/data/index.js"), content, "utf8");
